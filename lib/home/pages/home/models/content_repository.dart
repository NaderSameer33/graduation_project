  static Future<List<ContentItem>> fetchArticlesFromApi(int conditionId) async {
    try {
      final res = await ApiService.authenticatedGet('patient_articles', id: conditionId.toString());
      if (res.success && res.asList.isNotEmpty) {
        return res.asList
            .map((e) => ContentItem.fromBackendJson(e, conditionId))
            .toList();
      }
    } catch (e) {
      // ignore: avoid_print
      print('Error fetching articles from API: \$e');
    }
    return [];
  }
